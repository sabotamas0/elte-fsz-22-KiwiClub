package KiwiClub.KiwiClub.Domain;

public class Food {

	protected int price;
	protected int nutrition;
	
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getNutrition() {
		return nutrition;
	}
	public void setNutrition(int nutrition) {
		this.nutrition = nutrition;
	}
	
}
